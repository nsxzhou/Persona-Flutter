以 3-5 章为一个悬念单元：压力出现 -> 局部兑现 -> 代价反噬 -> 抛出下一轮诱惑。连续章节不要重复同一种情节模式，要在压迫、调查、误判、反击、缓冲之间形成节奏曲线。任意时刻至少保持 2 条悬念线在运行。

## 钩子工程

13种章末钩子（`hookType` 使用这些名称，`chapterHook` 写具体内容）：
1. 突变揭露：抛出改变格局的信息（"信上的日期是他死后第七天。"）
2. 紧急危机：下一章必须立刻应对的紧迫状况
3. 中断动作：行动被新变量打断（"他伸出手——'别动。'身后传来声音。"）
4. 身份反转：某人的真实身份与认知不符
5. 两难困境：被迫在两个坏选项中选一个
6. 神秘物件：重要但意义未明的物品出现
7. 倒计时：时间即将耗尽
8. 承诺/威胁：某人宣布即将采取重大行动
9. 不可能消失：无法解释的失踪或消失
10. 暗含深意：表面正常但暗藏信息量
11. 意象钩子：反复出现的意象发生变化
12. 呼应钩子：结尾呼应开头，但关键细节已变
13. 留白钩子：只展示反应，不揭示发生了什么

7种章首钩子（规划下一章开头时使用）：
1. 悬念对话开场 2. 闪前碎片 3. 倒计时开场 4. 神秘独白
5. 反差场景 6. 中断动作续接 7. 意象伏笔

## 反转工具箱（在关键章节使用）
7种反转类型：
1. 身份反转：角色真实身份与读者认知不同。需提前埋 3+ 行为暗示，最佳位置 65-75%。
2. 视角反转：叙述者视角是片面的，真相从另一角度完全不同。最佳位置 70-80%。
3. 动机反转：角色真正原因与读者假设不同。通过高压二选一场景揭示。最佳位置 75-85%。
4. 时间线反转：实际时间顺序与读者理解不同。只调整叙述顺序不撒谎。最佳位置 80-90%。
5. 信息反转：读者（和主角）持有来自"可靠来源"的错误信息。最佳位置 70-80%。
6. 认知反转：读者对角色/关系的整体情感判断被翻转。在每个负面场景中埋被忽略的反向细节。最佳位置 75-90%。
7. 无反转（甜文/爽文/复仇文）：满足感来自因果报应的精确兑现或甜度递进。

反转铁律：50% 之前不揭露（铺垫不够），95% 之后不揭露（没时间展开）。最佳区间 70-85%。

## 情绪弧线设计
6种情绪弧线类型（`emotionArc` 使用这些类型）：
1. V形（虐 -> 治愈）：底部在 60-70% 处，治愈上升要快。
2. 倒V（爽 -> 反转 -> 低落）：顶部在 40-50% 处，下跌比上升快。
3. W形（多峰多谷）：至少 2 峰 1 谷，每个峰比前一个高，最终峰在 80-90%。
4. 递进式（阶梯上升）：不允许回退，像楼梯一样持续上升。
5. 延迟满足型：前 70-80% 压抑铺垫，后 20-30% 集中爆发。
6. 急转型：70-80% 正常弧线后突然方向变化，转折只用 1-2 段。

## 满足感设计
公式：制造欲望（低地位/困境/威胁） -> 给出希望（金手指/转机） -> 努力解决（不要解决太快） -> 得到想要的（爽感释放）。
每 3000-5000 字要有一次情绪兑现。满足感类型要交替使用（能力碾压/态度反转/身份揭露/收获盘点等），不要连续同一种。

## 字段写法
- `objective` 写本章必须完成的叙事动作——如果删掉这章故事不受影响，说明目标设计有问题。
- `pressureSource` 写即时压力来自谁、什么规则或哪个倒计时。要具体。
- `payoffTarget` 写本章给读者的明确兑现（线索、关系、资源、反击或真相碎片）。不能是空的"推进剧情"。
- `relationshipShift` 写关系如何变化；没有变化时也要写"维持但压力升级"的具体原因。
- `hookType` 从 13 种章末钩子中选一个具体类型名称。
- `chapterHook` 用一两句话描述具体钩子内容，禁止只写"留下悬念""暗流涌动"。
- `coreEvent` 写不可删除的核心事件——一两句话，要有画面感。
- `emotionArc` 写本章情绪如何转向，可用弧线类型 + 具体描述。
- `outlineMarkdown` 写可执行场景步骤，包含：场景推进（谁在哪做什么）、冲突选择（主角面临什么抉择）、伏笔埋设或回收（具体操作）、情绪转折（从什么情绪转到什么情绪）。每个子事件要把"发生了什么""主角注意到什么感官细节""身体如何反应"编织在同一个段落里，禁止分三段写。

## 反AI腔写作规则
生成 outlineMarkdown 时遵循以下规则，因为这些指导会直接影响最终正文质量：
- 禁用词：仿佛、犹如、宛若、缓缓、不禁、微微、淡淡、映入眼帘、心中暗道、嘴角微扬、瞳孔微缩、不由自主、心头一震、心下了然。
- 禁用句式："不是A，而是B"（最强AI腔信号）；"声音不大，却带着...的力量"；"眼中闪过一丝..."；"嘴角勾起一抹..."。
- 心理描写外化：不写"他感到愤怒"，写身体细节（"手背上的青筋鼓起来"）。
- 段落节奏：优先一句一段；超过60字的段落考虑分行；约 30-50% 的句子独立成段。
- 章末禁止总结感慨、哲理收尾、"他不知道的是..."式预告。用动作、对话或悬念收尾。
- 每个子事件禁止分三段写（发生/感知/反应分开是AI腔标志性模式），必须编织在一个连续段落里。

## 反道德说教
- 主角的决策逻辑写利益计算，不写道德挣扎。可以写"他算了一下得失"，不要写"他内心在正义和私利之间挣扎"。
- 冲突解决靠实力/信息差/资源碾压，不靠"正义觉醒"或"天理昭昭"。
- 不要写主角"虽然想报复但还是选择了原谅"这种道德升华。让主角该报复就报复，该利用就利用。
- 配角的帮助和支持要有利益理由，不要写成"因为主角是好人所以大家都帮他"。
- 场景描述用利益语言（"他控制了这个街区"）而非道德语言（"他是这片区域的正义守护者"）。
- payoffTarget 中的爽感来自实力展示和利益获取，不来自"好人终于得到回报"。

