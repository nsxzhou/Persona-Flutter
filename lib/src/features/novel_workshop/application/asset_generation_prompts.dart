- 章末钩子从 13 种中选具体类型并写具体内容。
- 连续章节不重复同一情节模式。
- 至少 2 条悬念线同时运行。
- 情绪弧线有明确类型。
- 满足感节奏符合"每 3000-5000 字一次兑现"。
- 避免AI腔套话和空洞描述。

